<h3>Input Option (a) - Command Line Arguments</h3>
<p>Command line agruments are the arguments which can be passed at the command prompt along with program name, for example</p>
<code>
$programname arg1 arg2 arg3....
</code>
<p>These arguments will be separated by space and you can parse these arguments inside your program and use them as per your requirements. For example:</p>
<code>
$perl test.pl argument1 argument2
</code>